package com.rudra.calc.bs.report.output;

public class DepositReport {
	private double totalInvestmentCollected;
	private double depositAmount;
	private double intrestOnDeposit;
	private double grossDepostiAmount;

	public double getTotalInvestmentCollected() {
		return totalInvestmentCollected;
	}

	public void setTotalInvestmentCollected(double totalInvestmentCollected) {
		this.totalInvestmentCollected = totalInvestmentCollected;
	}

	public double getDepositAmount() {
		return depositAmount;
	}

	public void setDepositAmount(double depositAmount) {
		this.depositAmount = depositAmount;
	}

	public double getIntrestOnDeposit() {
		return intrestOnDeposit;
	}

	public void setIntrestOnDeposit(double intrestOnDeposit) {
		this.intrestOnDeposit = intrestOnDeposit;
	}

	public double getGrossDepostiAmount() {
		return grossDepostiAmount;
	}

	public void setGrossDepostiAmount(double grossDepostiAmount) {
		this.grossDepostiAmount = grossDepostiAmount;
	}

}
